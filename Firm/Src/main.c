void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
	uint8_t res;
	if(AddrMatchCode == DEV_ADR)
	{
		if(TransferDirection == I2C_DIRECTION_TRANSMIT)
		{
			res=HAL_I2C_Slave_Seq_Receive_IT(&hi2c3, I2CRxBuffer, 1, I2C_FIRST_FRAME);//I2C_FIRST_FRAME
			if(res!=0)Error_Handler();
			regOffset=0;
		}
		else
		{
			res=HAL_I2C_Slave_Seq_Transmit_IT(&hi2c3, &I2CRegData[offset], sizeof(I2CRegData)-offset, I2C_LAST_FRAME);
			if(res!=0)Error_Handler();
			regOffset=0;
		}
	}
}

void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	if(hi2c->Instance==hi2c3.Instance)
	{
		offset=I2CRxBuffer[0];
		if(offset>7)offset=7;

		if(regOffset<I2CRXBUFFERSIZE)
			regOffset++;
		HAL_I2C_Slave_Seq_Receive_IT(&hi2c3, &I2CRxBuffer[regOffset], 1, I2C_FIRST_AND_LAST_FRAME);
		if(offset==4 && regOffset>2)
		{
			uint16_t val16 = (uint16_t)I2CRxBuffer[1]*256+I2CRxBuffer[2];
			setActivityValue((float)val16*0.1f);
		}
	}
}

