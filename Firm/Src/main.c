	intiCicFilterAll();

	getAdcValue();
	//Pre Operation
	startPDM();
	HAL_Delay(100);
	disableDMA();
	HAL_Delay(100);

    // FFT init
    float SPI1_BaudRate = (float)SystemCoreClock / 1.0f / 128.0f;//SPI_BAUDRATEPRESCALER_128
